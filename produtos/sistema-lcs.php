      <p>Pelo fato de ser um sistema modular, apresenta grande vantagem em sua instalação, manutenção e principalmente numa eventual necessidade de alteração ou expansão de projeto, pois através de uma simples inclusão ou remanejo de alguns componentes, permite que o equipamento se ajuste a uma nova necessidade de aplicação.</p>
