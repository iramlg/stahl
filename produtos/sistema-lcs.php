      <h3>Alta tecnologia e flexibilidade</h3>
      <p>O Sistema LCS (Light Crane System), é um sistema modular para construção de equipamentos de movimentação de carga, composto basicamente por: <strong>Perfil LCS</strong>, laminado a frio de baixo peso próprio; <strong>Trole especial</strong>, com rodas de baixo atrito; <strong>Suspensão articulada</strong>, capaz de absorver praticamente todos os esforços horizontais nos pontos de apoio; <strong>Componentes auxiliares</strong>, tais como olhais, grampos, uniões aparafusadas e tampas, cuja fabricação e projeto são totalmente padronizados.</p>
